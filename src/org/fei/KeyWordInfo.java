package org.fei;

public class KeyWordInfo {

    public double p;
    public double q;
    public int wordNumber;
    public WordInfo wordInfo;

    public KeyWordInfo(double p, double q, int wordNumber, WordInfo wordInfo) {
        this.p = p;
        this.q = q;
        this.wordNumber = wordNumber;
        this.wordInfo = wordInfo;
    }
}
